const getRandomColor = () => {
  const letters = "0123456789ABCDEF";
  let color = "#";
  for (let i = 0; i < 6; i++) {
    color += letters[Math.floor(Math.random() * 16)];
  }
  return color;
};

const Level16 = ({ uid, loading_comp }) => {
  const canvasRef = useRef(null);
  const [boxes, setBoxes] = useState([]);
  const [inputValue, setInputValue] = useState("");
  const [message, setMessage] = useState("");
  const [dimensions, setDimensions] = useState({
    width: window.innerWidth,
    height: window.innerHeight - 100, // Scade înălțimea pentru a face loc inputului
  });

  // --- Funcția de desenare pe canvas ---
  const drawCanvas = useCallback((ctx, boxesToDraw, width, height) => {
    ctx.clearRect(0, 0, width, height);
    ctx.fillStyle = "#350606"; // Fundal întunecat
    ctx.fillRect(0, 0, width, height);

    boxesToDraw.forEach((box) => {
      ctx.fillStyle = box.color; // Folosește culoarea stocată a pătratului
      ctx.fillRect(box.x, box.y, box.size, box.size);
    });
  }, []);

  // --- Funcție pentru gestionarea click-ului pe canvas ---
  const handleCanvasClick = useCallback(
    (event) => {
      const canvas = canvasRef.current;
      if (!canvas) return;

      // Obține poziția click-ului relativ la canvas
      const rect = canvas.getBoundingClientRect();
      const clickX = event.clientX - rect.left;
      const clickY = event.clientY - rect.top;

      // Iterează prin pătrate pentru a vedea dacă a fost apăsat vreunul
      for (let i = 0; i < boxes.length; i++) {
        const box = boxes[i];
        // Verifică dacă click-ul este în interiorul pătratului
        if (
          clickX >= box.x &&
          clickX <= box.x + box.size &&
          clickY >= box.y &&
          clickY <= box.y + box.size
        ) {
          // Dacă s-a apăsat pe un pătrat, afișează elementul corespunzător din PHRASE
          console.log(PHRASE[i]);
          // Oprește căutarea după ce a fost găsit pătratul apăsat
          return;
        }
      }
    },
    [boxes] // Dependența de 'boxes' este necesară pentru a avea acces la starea actuală a pătratelor
  );

  // --- Efect pentru actualizarea dimensiunilor la redimensionarea ferestrei ---
  useEffect(() => {
    const handleResize = () => {
      setDimensions({
        width: window.innerWidth,
        height: window.innerHeight - 100, // Ajustează înălțimea la redimensionare
      });
    };

    window.addEventListener("resize", handleResize);
    return () => window.removeEventListener("resize", handleResize);
  }, []);

  // --- Efect pentru generarea pătratelor bazat pe lungimea PHRASE și dimensiuni ---
  useEffect(() => {
    const totalBoxes = PHRASE.length;
    const padding = 0; // Setăm padding-ul la 0 pentru a acoperi complet spațiul

    let bestNumCols = 1;
    let largestBoxSize = 0;

    // Caută numărul optim de coloane pentru a maximiza dimensiunea pătratelor
    for (
      let numColsCandidate = 1;
      numColsCandidate <= totalBoxes;
      numColsCandidate++
    ) {
      const numRowsCandidate = Math.ceil(totalBoxes / numColsCandidate);

      // Calculează dimensiunea potențială a pătratului
      const potentialBoxSize = Math.min(
        dimensions.width / numColsCandidate,
        dimensions.height / numRowsCandidate
      );

      // Dacă această configurație oferă o dimensiune mai mare și se încadrează, o reținem
      if (potentialBoxSize > largestBoxSize) {
        largestBoxSize = potentialBoxSize;
        bestNumCols = numColsCandidate;
      }
    }

    // Folosește cea mai bună configurație găsită
    const numCols = bestNumCols;
    const numRows = Math.ceil(totalBoxes / numCols);
    const actualBoxSize = largestBoxSize; // Dimensiunea pătratului care maximizează acoperirea

    const newBoxes = [];
    // Pozițiile de start sunt (0,0) deoarece pătratele vor umple întreg spațiul
    const startX = 0;
    const startY = 0;

    // Generează pătratele
    for (let i = 0; i < totalBoxes; i++) {
      const row = Math.floor(i / numCols);
      const col = i % numCols;

      // Calculează poziția
      const bx = startX + col * (actualBoxSize + padding);
      const by = startY + row * (actualBoxSize + padding);

      const color = getRandomColor();
      newBoxes.push({ x: bx, y: by, size: actualBoxSize, color });
    }

    setBoxes(newBoxes);
  }, [dimensions, PHRASE.length]); // Dependența de PHRASE.length asigură regenerarea dacă array-ul s-ar schimba

  // --- Efect pentru schimbarea culorilor la interval ---
  useEffect(() => {
    const intervalId = setInterval(() => {
      setBoxes((currentBoxes) =>
        currentBoxes.map((box) => ({
          ...box,
          color: getRandomColor(),
        }))
      );
    }, 60000); // 60 secunde

    return () => clearInterval(intervalId);
  }, []);

  // --- Efect pentru desenarea/redesenarea canvasului și adăugarea listenerului de click ---
  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;

    canvas.width = dimensions.width;
    canvas.height = dimensions.height;

    drawCanvas(ctx, boxes, dimensions.width, dimensions.height);

    // Adaugă event listenerul pentru click
    canvas.addEventListener("click", handleCanvasClick);

    // Curăță event listenerul la demontarea componentei sau la schimbarea handlerului
    return () => {
      canvas.removeEventListener("click", handleCanvasClick);
    };
  }, [boxes, dimensions, drawCanvas, handleCanvasClick]); // Dependențe actualizate

  // --- Handler pentru schimbarea valorii în input ---
  const handleInputChange = (e) => {
    setInputValue(e.target.value);
    if (message) {
      setMessage("");
    }
  };
  
  useEffect(() => {
    if (!loading_comp) startLevel(uid, getLevel());
  }, [, loading_comp]);
  
  const handleCheckSolution = () => {
    updateLevel(
      inputValue.trim().toLowerCase(),
      uid,
      getLevel() + 1,
      "mediu"
    ).then((res) => {
      if (res.data.ok) {
        alert(res.data.message);
        to(`/level${getLevel() + 1}`);
      } else {
        alert(res.data.message);
      }
    });
  };

  const hint = async (uid, level) => {
    await getHint(uid, level).then((res) => {
      if (res.data.ok) {
        alert(res.data.hint);
      } else {
        alert(res.data.message);
      }
    });
  };

  return (
    <div className="level">
      {!loading_comp ? (
        <div className="level2">
          <div className="cerinta hint" onClick={() => hint(uid, getLevel())}>
            <img
              src={require("../../assets/img/styling/hint.svg").default}
              alt=""
            />
          </div>
          <Poveste />

          <canvas
            ref={canvasRef}
            style={{
              display: "block",
              maxWidth: "100%",
            }}
          />
          <div className="submit">
            <input
              type="text"
              value={inputValue}
              onChange={handleInputChange}
              placeholder="Răspunsul tău..."
            />
            <button className="button main" onClick={handleCheckSolution}>
              <span>Submit</span>
            </button>
          </div>
        </div>
      ) : (
        <>Loading</>
      )}
    </div>
  );
};

export default Level16;
